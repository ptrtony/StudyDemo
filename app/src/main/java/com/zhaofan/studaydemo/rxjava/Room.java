public class Room {
    private double price;
    private double time;
    private boolean doubleSleep;

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public double getTime() {
        return time;
    }

    public void setTime(double time) {
        this.time = time;
    }

    public boolean isDoubleSleep() {
        return doubleSleep;
    }

    public void setDoubleSleep(boolean doubleSleep) {
        this.doubleSleep = doubleSleep;
    }
}
